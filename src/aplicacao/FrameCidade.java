    private void InserirCidadeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_InserirCidadeMouseClicked
        // TODO add your handling code here:
            Cidade cidade = new Cidade();
        int z = Integer.parseInt(txtCod.getText());
        cidade.setCodCidade(z);
        cidade.setNome(txtNome.getText());
        cidade.setEstado(txtEstado.getText());
       
       Cidade c = cidadeDAO.busca(getID());
        if (c == null) {
          cidadeDAO.inserir(cidade);
          JOptionPane.showMessageDialog(this,"Inserido cidade, com sucesso!");
          limpa();  
        }else{
         JOptionPane.showMessageDialog(this,"Codigo da Cidade jÃ¡ Existe!");
        }
       
       
    }//GEN-LAST:event_InserirCidadeMouseClicked

