public int getID() {
        int id = Integer.parseInt(txtCod.getText());
        
        return id;
        
    }
    public Cidade leCidade(){
        Cidade cidade = new Cidade();
        int z = Integer.parseInt(txtCod.getText());
        cidade.setCodCidade(z);
        cidade.setNome(txtNome.getText());
        cidade.setEstado(txtEstado.getText());
        return cidade;
    
    }
    
    public void exibeCidade(Cidade cidade){
        txtCod.setText(String.valueOf(cidade.getCodCidade()));
        txtNome.setText(cidade.getNome());
        txtEstado.setText(cidade.getEstado());
        
        }
