    private void inicializa(){
        cbCliente.removeAllItems();
        LinkedList<Cliente> clientes = clienteDAO.busca();
        if(clientes!=null){
        for (Cliente cliente : clientes) {
            cbCliente.addItem(cliente.getCodCliente() + " - " + cliente.getNome());
        }
        }
        cbProduto.removeAllItems();
        LinkedList<Produto> produtos = produtoDAO.busca();
        if(produtos!=null){
        for (Produto produto : produtos) {
            cbProduto.addItem(produto.getCodProduto()+ " - " + produto.getDescricao());
        }}
    }

