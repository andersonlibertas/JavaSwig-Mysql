    private void jLabel7MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel7MouseClicked
        // TODO add your handling code here:
          // TODO add your handling code here:
        Venda venda = new Venda();
        venda.setNumNota(Integer.parseInt(txtNumeroNota.getText()));
        String cliStr = cbCliente.getSelectedItem().toString();
        String cliVet[] = cliStr.split(" - ");
        int cod = Integer.parseInt(cliVet[0]);
        Cliente cliente = clienteDAO.busca(cod);
        venda.setCliente(cliente);
        venda.setData(data);
        venda.setListaProdutos(produtos);
        venda.setListaQuantidades(quantidades);

        int r = vendaDAO.inserir(venda);
        if (r > 0) {
            JOptionPane.showMessageDialog(null, "Venda " + venda.getNumNota()
                + " inserida com sucesso");
        } else if (r == -1) {
            JOptionPane.showMessageDialog(null, "Não inserido");
        }

        limpa();
    }//GEN-LAST:event_jLabel7MouseClicked

