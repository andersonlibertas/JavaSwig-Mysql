    private void btAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdicionarActionPerformed
        String produtoStr = cbProduto.getSelectedItem().toString();
        String produtoVet[] = produtoStr.split(" - ");
        int idProduto = Integer.parseInt(produtoVet[0]);
        Produto produto = produtoDAO.busca(idProduto);
        produtos.add(produto);
        int qtd = Integer.parseInt(txtQuantidade.getText());
        quantidades.add(qtd);
        tabela.setValueAt(produto.getCodProduto(), linha, 0);
        tabela.setValueAt(produto.getDescricao(), linha, 1);
        tabela.setValueAt(produto.getPreco(), linha, 2);
        tabela.setValueAt(produto.getSaldo(), linha, 3);
        tabela.setValueAt(produto.getMarca().getNomeMarca(), linha, 4);
        linha++;
    }//GEN-LAST:event_btAdicionarActionPerformed

