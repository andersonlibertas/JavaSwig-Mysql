    private void ConsultarClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ConsultarClienteMouseClicked
        // TODO add your handling code here:
        LinkedList<Cliente> lista = clienteDAO.busca();
        DefaultTableModel modelo =
        (DefaultTableModel)tabelaCliente.getModel();
        modelo.setNumRows(lista.size());

        for (int i = 0; i < lista.size(); i++) {
            Cliente c = lista.get(i);
            tabelaCliente.setValueAt(c.getCodCliente(), i, 0);
            tabelaCliente.setValueAt(c.getNome(),i,1);
            tabelaCliente.setValueAt(c.getTelefone(),i,2);
            Cidade cid = c.getCidade();
            tabelaCliente.setValueAt(cid.getNome(),i,3);
        }
    }//GEN-LAST:event_ConsultarClienteMouseClicked

