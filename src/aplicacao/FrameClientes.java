    private Cliente leCliente(){
        Cliente cliente = new Cliente();
        int codigo = Integer.parseInt(txtCodigo.getText());
        String nome = txtNome.getText();
        String telefone = txtTelefone.getText();
        String cidadeStr = cbCidade.getSelectedItem().toString();
        String cidadeVet[] = cidadeStr.split(" - ");
        int cidadeCod = Integer.parseInt(cidadeVet[0]);
        Cidade cidade = cidadeDAO.busca(cidadeCod);
        
        cliente.setCodCliente(codigo);
        cliente.setTelefone(telefone);
        cliente.setNome(nome);
        cliente.setCidade(cidade);
        return cliente;
    }
            
